export const getPollutionLevel = (hmpi) => {
  if (hmpi >= 80) return "critical";
  if (hmpi >= 65) return "high";
  if (hmpi >= 45) return "medium";
  return "low";
};

export const getPollutionColor = (hmpi) => {
  const level = getPollutionLevel(hmpi);
  const colors = {
    low: "hsl(var(--pollution-low))",
    medium: "hsl(var(--pollution-medium))",
    high: "hsl(var(--pollution-high))",
    critical: "hsl(var(--pollution-critical))"
  };
  return colors[level];
};